<?php

// Operator Penugasan
// Operator Penugasan adalah operator yang digunakan untuk memberikan nilai pada variabel
// Operator Penugasan terdiri dari 5 jenis, yaitu:
// 1. Penugasan (=)
// 2. Penugasan Tambah (+=)
// 3. Penugasan Kurang (-=)
// 4. Penugasan Kali (*=)
// 5. Penugasan Bagi (/=)

// Contoh Penggunaan Operator Penugasan
$a = 10;
$b = 20;

echo "Nilai a: " . $a;
echo "<br>";
echo "Nilai b: " . $b;
echo "<br>";

// Penugasan
$a = $b;
echo "Setelah Penugasan, Nilai a: " . $a;
echo "<br>";

// Penugasan Tambah
$a += $b;
echo "Setelah Penugasan Tambah, Nilai a: " . $a;
echo "<br>";

// Penugasan Kurang
$a -= $b;
echo "Setelah Penugasan Kurang, Nilai a: " . $a;
echo "<br>";

// Penugasan Kali
$a *= $b;
echo "Setelah Penugasan Kali, Nilai a: " . $a;
echo "<br>";

// Penugasan Bagi
$a /= $b;
echo "Setelah Penugasan Bagi, Nilai a: " . $a;
echo "<br>";
echo "<hr>";

// Contoh Lagi
$c = 50;

echo "Nilai c: " . $c;
echo "<br>";

// Penugasan
$c;
echo "Setelah Penugasan, Nilai c: " . $c;
echo "<br>";

// Penugasan Tambah
$c += 10;
echo "Setelah Penugasan Tambah, Nilai c: " . $c;
echo "<br>";

// Penugasan Kurang
$c -= 5;
echo "Setelah Penugasan Kurang, Nilai c: " . $c;
echo "<br>";

// Penugasan Kali
$c *= 2;
echo "Setelah Penugasan Kali, Nilai c: " . $c;
echo "<br>";

// Penugasan Bagi
$c /= 10;
echo "Setelah Penugasan Bagi, Nilai c: " . $c;
echo "<br>";

?>

