<?php

// Operator Aritmatika
// Operator Aritmatika adalah operator yang digunakan untuk melakukan operasi matematika
// Operator Aritmatika terdiri dari 5 jenis, yaitu:
// 1. Penjumlahan (+)
// 2. Pengurangan (-)
// 3. Perkalian (*)
// 4. Pembagian (/)
// 5. Modulus (%)

// Contoh Penggunaan Operator Aritmatika
$a = 100;
$b = 5;

echo "Penjumlahan: " . ($a + $b);
echo "<br>";
echo "Pengurangan: " . ($a - $b);
echo "<br>";
echo "Perkalian: " . ($a * $b);
echo "<br>";
echo "Pembagian: " . ($a / $b);
echo "<br>";
echo "Modulus: " . ($a % $b);

?>